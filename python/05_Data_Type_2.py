# 실습 예제 1: 집합 만들기
set1 = {1, 2, 3}  # 중괄호 {}를 사용하여 집합을 생성합니다. 집합은 중복을 허용하지 않습니다.
print(set1)  # set1을 출력합니다: {1, 2, 3}

# 실습 예제 2: 집합 연산
set2 = {2, 3, 4}  # 또 다른 집합 set2를 생성합니다.
print(set1 & set2)  # set1과 set2의 교집합 {2, 3}을 출력합니다.
print(set1 | set2)  # set1과 set2의 합집합 {1, 2, 3, 4}을 출력합니다.
print(set1 - set2)  # set1에서 set2를 뺀 차집합 {1}을 출력합니다.
print(set1 ^ set2)  # set1과 set2의 대칭 차집합 {1, 4}을 출력합니다.

# 실습 예제 3: 집합 관련 함수
set1.add(4)  # set1에 요소 4를 추가합니다.
print(set1)  # {1, 2, 3, 4}를 출력합니다.
set1.remove(2)  # set1에서 요소 2를 제거합니다.
print(set1)  # {1, 3, 4}를 출력합니다.
print(len(set1))  # set1의 길이 3을 출력합니다.

# 실습 예제 4: 불 자료형
is_true = True  # 불 값 True를 is_true에 할당합니다. 참을 나타냅니다.
is_false = False  # 불 값 False를 is_false에 할당합니다. 거짓을 나타냅니다.
print(is_true)  # True를 출력합니다.
print(is_false)  # False를 출력합니다.

# 실습 예제 5: 불 연산
print(1 == 1)  # 1과 1이 같은지 비교하여 True를 출력합니다.
print(1 != 2)  # 1과 2가 다른지 비교하여 True를 출력합니다.
print(5 > 3 and 2 < 4)  # 두 조건이 모두 참인지 and 연산으로 확인하여 True를 출력합니다.
print(5 > 3 or 2 > 4)  # 두 조건 중 하나라도 참인지 or 연산으로 확인하여 True를 출력합니다.
print(not True)  # True의 반대인 False를 not 연산으로 출력합니다.

# 실습 예제 6: 자료형의 참과 거짓
print(bool(0))  # 정수 0은 False로 평가됩니다.
print(bool(1))  # 정수 1은 True로 평가됩니다.
print(bool(""))  # 빈 문자열은 False로 평가됩니다.
print(bool("hello"))  # 비어 있지 않은 문자열은 True로 평가됩니다.
print(bool([]))  # 빈 리스트는 False로 평가됩니다.
print(bool([1, 2]))  # 비어 있지 않은 리스트는 True로 평가됩니다.

# 실습 예제 7: 변수 만들기
x = 10  # 정수 10을 변수 x에 할당합니다.
y = "Hello"  # 문자열 "Hello"를 변수 y에 할당합니다.
print(x, y)  # x와 y의 값을 공백으로 구분하여 출력합니다.

# 실습 예제 8: 리스트 복사
original = [1, 2, 3]  # 리스트 original을 생성합니다.
copy_list = original[:]  # 슬라이싱을 사용하여 original의 얕은 복사를 만듭니다.
print(copy_list)  # 복사된 리스트 [1, 2, 3]을 출력합니다.

# 실습 예제 9: 변수의 효력 범위 (주석으로 설명)
# 함수 안에서 정의된 변수는 지역 변수로, 함수 밖에서 사용할 수 없습니다. 함수 밖에서 정의된 변수는 전역 변수입니다.

# 실습 예제 10: 여러 가지 방법으로 변수 만들기
a, b, c = 1, 2, 3  # 튜플 언패킹을 사용하여 한 줄에 여러 변수를 할당합니다.
print(a, b, c)  # a=1, b=2, c=3을 출력합니다.

