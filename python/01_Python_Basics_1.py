# 실습 예제 6: 간단한 리스트 사용
fruits = ["apple", "banana", "cherry"]  # 리스트를 생성하여 변수 fruits에 할당합니다.
print("과일 목록:", fruits)  # 리스트 전체를 출력합니다.
print("첫 번째 과일:", fruits[0])  # 리스트의 첫 번째 요소를 인덱스 0으로 접근하여 출력합니다.

# 실습 예제 7: 조건문 예제
age = 25  # 정수 25를 변수 age에 저장합니다.
if age >= 20:  # age가 20 이상인지 조건을 검사합니다.
    print("성인입니다.")  # 조건이 참이면 이 문장을 출력합니다.
else:  # 그렇지 않으면
    print("미성년자입니다.")  # 이 문장을 출력합니다.

