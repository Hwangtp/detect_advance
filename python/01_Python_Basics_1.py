# 실습 예제 1: 파이썬의 간단한 인사말 출력
print("Hello, Python!")  # print 함수를 사용하여 문자열 "Hello, Python!"을 콘솔에 출력합니다.

# 실습 예제 2: 변수에 값 저장하고 출력
name = "Python"  # 문자열 "Python"을 변수 name에 할당합니다.
print("Welcome to", name)  # print 함수로 "Welcome to"와 변수 name의 값을 함께 출력합니다.

# 실습 예제 3: 기본 산술 연산
a = 10  # 정수 10을 변수 a에 저장합니다.
b = 5   # 정수 5를 변수 b에 저장합니다.
print("덧셈:", a + b)  # a와 b의 합을 계산하여 출력합니다. 결과: 15
print("뺄셈:", a - b)  # a에서 b를 뺀 값을 출력합니다. 결과: 5
print("곱셈:", a * b)  # a와 b의 곱을 출력합니다. 결과: 50
print("나눗셈:", a / b)  # a를 b로 나눈 실수 결과를 출력합니다. 결과: 2.0

# 실습 예제 4: 파이썬의 특징 설명 (주석으로)
