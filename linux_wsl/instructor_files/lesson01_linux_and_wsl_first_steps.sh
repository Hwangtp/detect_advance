#!/bin/bash
# WSL Ubuntu 기준 강사용 실습 파일
# 이 파일은 한 줄씩 직접 입력하며 설명하는 수업 노트다.
# 자동 실행용이라기보다 강사용 실습 가이드로 사용한다.
# 모든 실습은 WSL Ubuntu 터미널에서 진행하는 것을 기준으로 한다.

# 수업 제목: 리눅스와 WSL 첫걸음
# 수업 목표: 리눅스가 무엇인지 이해하고 WSL Ubuntu 터미널에 익숙해진다.
# 수업 개요: 리눅스의 역할, WSL Ubuntu의 의미, 터미널 프롬프트, 현재 사용자와 운영체제 정보를 읽는 실습을 진행한다.

# 핵심 개념
# - 리눅스는 서버, 개발, 배포 환경에서 많이 사용하는 운영체제이다.
# - WSL은 Windows 안에서 Linux 환경을 사용할 수 있게 해주는 기능이다.
# - 터미널은 마우스보다 명령어로 시스템을 다루는 화면이다.
# - 루트 디렉터리 / 는 리눅스 전체의 시작점이고 홈 디렉터리 ~ 는 현재 계정의 개인 작업 공간이다.

# 핵심 실습 명령어

# pwd는 print working directory의 약자이며 현재 작업 위치를 출력한다. 옵션 없이 자주 사용한다.
pwd

# whoami는 현재 로그인한 사용자 이름을 확인한다. 권한 관련 실습에서 매우 중요하다.
whoami

# echo $USER 는 현재 로그인한 계정 이름을 변수로 확인하는 방법이다. whoami와 결과를 비교해 볼 수 있다.
echo $USER

# uname은 시스템 정보를 보여준다. -a 옵션은 커널, 아키텍처 등 가능한 많은 정보를 함께 출력한다.
uname -a

# cat은 파일 내용을 그대로 출력한다. /etc/os-release는 배포판 정보가 들어 있는 대표 파일이다.
cat /etc/os-release

# hostname은 현재 컴퓨터 이름을 확인한다. 서버 식별에 자주 사용한다.
hostname

# date는 현재 시스템 날짜와 시간을 출력한다. 로그 해석과 예약 작업에서 자주 쓴다.
date

# ls는 목록을 보는 명령어다. /는 루트 디렉터리이며 리눅스 최상위 위치다.
ls /

# WSL Ubuntu에서는 일반 사용자 홈 폴더가 /home 아래에 생성된다.
ls /home

# echo는 문자열이나 변수 값을 출력한다. $HOME은 현재 사용자의 홈 디렉터리 경로다.
echo $HOME

# 현재 계정 홈 경로를 직접 조합해 보는 실습이다. $USER와 $HOME의 관계를 눈으로 확인한다.
echo /home/$USER

# ls -ld 의 -d 옵션은 디렉터리 자체 정보를 보여준다. 루트, home, 현재 계정 홈을 한 번에 비교한다.
ls -ld / /home ~

# cd는 디렉터리를 이동한다. ~는 홈 디렉터리를 의미한다.
cd ~

# 홈 디렉터리로 이동한 뒤 현재 위치가 정말 홈인지 다시 확인한다.
pwd

# 루트 디렉터리로 이동해 홈과 다른 출발점이라는 점을 직접 확인한다.
cd /

# 루트 디렉터리에서는 / 가 출력되는지 확인한다.
pwd

# mkdir은 폴더를 만든다. -p 옵션은 중간 폴더가 없어도 한 번에 생성한다.
mkdir -p ~/linux_lab/day01

# 실습 폴더로 이동해 이후 작업을 한곳에 모은다.
cd ~/linux_lab/day01

# touch는 빈 파일을 만든다. 기존 파일이 있으면 수정 시간만 갱신한다.
touch welcome.txt

# 리다이렉션 > 는 명령어 결과를 파일에 덮어쓴다. 첫 실습 파일 내용을 만든다.
echo 'Linux class starts here.' > welcome.txt

# 방금 만든 파일의 내용을 다시 확인한다.
cat welcome.txt

# 개인 실습 문제

# 문제 1. 현재 사용자 이름, 운영체제 이름, 홈 디렉터리 경로를 각각 확인해 보고 세 값을 노트에 적어보자.
# 문제 2. ~/linux_lab/day01 안에 intro.txt 파일을 만들고 자기소개 한 줄을 저장해 보자.
# 문제 3. 루트 디렉터리와 홈 디렉터리의 차이를 직접 이동해 보며 설명해 보자.
# 문제 4. /, /home, ~ 세 위치를 ls -ld 로 비교하고 어떤 곳이 개인 작업 공간인지 말해 보자.

# 정답 예시

# 정답 1. whoami, cat /etc/os-release, echo $HOME 순서로 확인할 수 있다.
# 정답 2. touch intro.txt 후 echo '안녕하세요 리눅스 수업을 시작합니다.' > intro.txt 를 입력하면 된다.
# 정답 3. cd / 는 시스템 최상위 위치로 이동하고 cd ~ 는 현재 사용자 홈으로 이동한다.
# 정답 4. ls -ld / /home ~ 를 보면 / 는 전체 시작점, /home 은 사용자 홈 모음, ~ 는 내 계정 전용 작업 공간임을 확인할 수 있다.
