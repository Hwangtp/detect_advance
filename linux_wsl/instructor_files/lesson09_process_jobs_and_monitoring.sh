#!/bin/bash
# WSL Ubuntu 기준 강사용 실습 파일
# 이 파일은 한 줄씩 직접 입력하며 설명하는 수업 노트다.
# 자동 실행용이라기보다 강사용 실습 가이드로 사용한다.
# 모든 실습은 WSL Ubuntu 터미널에서 진행하는 것을 기준으로 한다.

# 수업 제목: 프로세스 작업 제어 모니터링
# 수업 목표: 실행 중인 프로그램을 확인하고 종료하거나 백그라운드로 보낸다.
# 수업 개요: ps, top, kill, jobs를 사용해 프로세스 관리 감각을 익힌다.

# 핵심 개념
# - 프로세스는 실행 중인 프로그램이다.
# - 서버 운영에서는 실행 상태 확인과 종료가 매우 중요하다.

# 핵심 실습 명령어

# ps는 현재 셸 기준 프로세스를 본다.
ps

# ps -ef 는 전체 프로세스를 자세히 본다. -e는 all, -f는 full format이다.
ps -ef | head

# top은 실시간 프로세스 모니터링 도구다. q로 종료한다.
top

# & 는 명령을 백그라운드에서 실행한다. sleep은 대기 프로세스 예시로 좋다.
sleep 300 &

# jobs는 현재 셸에서 백그라운드로 돌고 있는 작업을 보여준다.
jobs

# grep으로 특정 프로세스를 찾아본다.
ps -ef | grep sleep

# kill은 프로세스를 종료한다. %1 은 jobs 목록의 첫 번째 작업 번호다.
kill %1

# 다시 백그라운드 작업을 하나 띄운다.
sleep 300 &

# jobs -l 의 -l 옵션은 PID까지 함께 보여준다.
jobs -l

# kill -9 는 강제 종료 신호다. jobs -p 는 PID만 출력한다. 강제 종료는 마지막 수단으로 쓴다.
kill -9 $(jobs -p)

# free는 메모리 사용량을 보여준다. -h 옵션은 human readable 형식이다.
free -h

# df는 디스크 사용량을 파일시스템별로 본다. -h 옵션은 보기 쉬운 단위다.
df -h

# 개인 실습 문제

# 문제 1. sleep 120 백그라운드 작업을 2개 띄우고 jobs로 확인해 보자.
# 문제 2. ps -ef | grep sleep 으로 PID를 찾은 뒤 kill로 종료해 보자.
# 문제 3. top, free -h, df -h 세 명령의 역할 차이를 설명해 보자.

# 정답 예시

# 정답 1. sleep 120 & 를 두 번 실행하면 된다.
# 정답 2. grep으로 PID를 확인한 뒤 kill PID 형식으로 종료하면 된다.
# 정답 3. top은 실시간 프로세스, free는 메모리, df는 디스크 사용량을 확인한다.
